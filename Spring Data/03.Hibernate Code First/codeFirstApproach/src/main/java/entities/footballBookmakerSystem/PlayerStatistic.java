package entities.footballBookmakerSystem;

import entities.BaseEntity;

import javax.persistence.*;

@Entity
@Table(name = "player_statistics")
public class PlayerStatistic extends BaseEntity {

    private int scoredGoals;
    private int playerAssists;
    private int playedMinutes;
    private Game game;
    private Player player;

    public PlayerStatistic() {
    }

    @Column(name = "scored_goals",nullable = false)
    public int getScoredGoals() {
        return scoredGoals;
    }

    public void setScoredGoals(int scoredGoals) {
        this.scoredGoals = scoredGoals;
    }

    @Column(name = "player_assists",nullable = false)
    public int getPlayerAssists() {
        return playerAssists;
    }

    public void setPlayerAssists(int playerAssists) {
        this.playerAssists = playerAssists;
    }

    @Column(name = "played_minutes",nullable = false)
    public int getPlayedMinutes() {
        return playedMinutes;
    }

    public void setPlayedMinutes(int playedMinutes) {
        this.playedMinutes = playedMinutes;
    }

    @ManyToOne
    @JoinColumn(name = "game_id", referencedColumnName = "id")
    public Game getGame() {
        return game;
    }

    public void setGame(Game game) {
        this.game = game;
    }

    @ManyToOne
    @JoinColumn(name = "player_id", referencedColumnName = "id")
    public Player getPlayer() {
        return player;
    }

    public void setPlayer(Player player) {
        this.player = player;
    }
}
